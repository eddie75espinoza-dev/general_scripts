import time

def countdown(time_sec):
    while time_sec:
        mins, secs = divmod(time_sec, 60)
        timeformat = '{:02d}:{:02d}'.format(mins, secs)
        print(timeformat, end='\r')
        time.sleep(1)
        time_sec -= 1
    print("\033[1;33m"+"\t stop"+"\033[0;37m") # salida en amarillo + reset de texto

countdown(7)
